import FS from 'fs';
import path from "path";

const build = (): void => {
    const ymlString = FS.readFileSync('../data/things.yml', 'utf-8');

    const configJsString = '// This file, things.yml.js, is generated by builder.js based on things.yml.\n  export default `' + ymlString + '`;';

    FS.writeFileSync(
        path.join(__filename, '..', 'data', 'things.yml.js'),
        configJsString,
        'utf8'
    );
};

build();
