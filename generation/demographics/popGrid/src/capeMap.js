    // one-off utility func.
    tidyCachedBox () {
        const oldBox = CapeMap.exampleBox();
        const newBox = [[]];

        for (let x = 0; x < oldBox.length; x++) {
            for (let y = 1; y < oldBox[x].length; y++) {
                const oldVal = oldBox[x][y];

                const newVal = oldVal < -999 ?
                    -1 :
                    Util.round(oldVal);

                newBox[x].push(newVal);
            }

            newBox.push([]);
        }

        Util.logDebug(newBox);
    }

